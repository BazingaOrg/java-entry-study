package com.itheima.operator;

public class OperatorDemo2 {
    /*
        关系运算符（比较运算符）：
            · >
            · >=
            · <
            · <=
            · ==
            · !=
    */
    public static void main(String[] args) {
        System.out.println(10 > 20);
        System.out.println(10 >= 20);
        System.out.println(10 < 20);
        System.out.println(10 <= 20);
        System.out.println(10 == 20);
        System.out.println(10 != 20);
    }
}
